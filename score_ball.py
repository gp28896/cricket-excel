class BallType(Enum):
    """Classification of a delivery."""

    LEGAL = auto()
    NO_BALL = auto()
    WIDE = auto()
    DEAD_BALL = auto()


class ExtraType(Enum):
    """Type of extra awarded."""

    NONE = auto()
    BYE = auto()
    LEG_BYE = auto()
    NO_BALL = auto()
    WIDE = auto()
    PENALTY = auto()


class WicketType(Enum):
    """Dismissal method."""

    NONE = auto()
    BOWLED = auto()
    CAUGHT = auto()
    LBW = auto()
    RUN_OUT = auto()
    STUMPED = auto()
    HIT_WICKET = auto()
    RETIRED_OUT = auto()
    TIMED_OUT = auto()
    OBSTRUCTING_FIELD = auto()
    HIT_BALL_TWICE = auto()


class InningsStatus(Enum):
    """Current innings state."""

    NOT_STARTED = auto()
    IN_PROGRESS = auto()
    COMPLETED = auto()


class MatchPhase(Enum):
    """Current match phase."""

    PRE_MATCH = auto()
    FIRST_INNINGS = auto()
    INTERVAL = auto()
    SECOND_INNINGS = auto()
    MATCH_COMPLETE = auto()


class BallOutcome(IntEnum):
    """High-level scoring outcome."""

    NORMAL = 0
    EXTRA = 1
    WICKET = 2
    EXTRA_AND_WICKET = 3


